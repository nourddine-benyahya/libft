#include "libft.h"
#include <stdlib.h>

void	*ft_calloc(size_t nmemb, size_t size)
{
	int	*ptr;

	ptr = (int *)malloc(nmemb * size);
	if (!ptr || !nmemb || !size)
		return (NULL);
	ft_bzero(ptr, nmemb);
	return (ptr);
}
